package thursday;

public class ArrayExample1 {

	public static void main(String[] args) {
		
		int[] myArray;
		
		myArray = new int[5];
		
		myArray[0] = 1;
		
		myArray[1] = 3;
		
		System.out.println("myArray[0]: " + myArray[0]);
		
		System.out.println("myArray[0]: " + myArray[1]);
		
		System.out.println("myArray[0]: " + myArray[2]);
		
		System.out.println("myArray[0]: " + myArray[3]);
		
		System.out.println("myArray[0]: " + myArray[4]);

	}

}
